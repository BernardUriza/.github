def render_dispatch_review():
    """Build the AI Review panel: repo + PR number + Run VAIR review.

    Layout mirrors AI Resolve's manual fallback row — input + primary
    action on a single horizontal line — instead of the previous vertical
    stack (LABEL / INPUT / BUTTON). The label was redundant with the
    placeholder; the button label now names the product (VAIR) and the
    user action (run a review) so the intent reads at a glance, same
    convention as 'Run VAIR' on AI Resolve cards.
    """
    panel = document["dispatch-review"]
    panel.clear()

    header = html.DIV(Class="runs-header")
    header <= html.H3("AI Review")
    actions = html.DIV(Class="runs-header-actions")
    actions <= _render_repo_select("review-repo")
    header <= actions
    panel <= header

    body = html.DIV(Class="dispatch-review-row")
    pr_input = html.INPUT(
        id="review-pr-manual",
        placeholder="PR #123",
        Class="dispatch-input",
        autocomplete="off",
        inputmode="numeric",
    )
    pr_input.attrs["type"] = "number"
    pr_input.attrs["min"] = "1"
    body <= pr_input
    btn = html.BUTTON(
        "Run VAIR review",
        Class="dispatch-btn",
        id="review-dispatch-btn",
        title="Posts /ai-review on the PR — VAIR workflow reads the comment and runs a code review",
    )
    btn.attrs["type"] = "button"
    btn.bind("click", _trigger_review)
    body <= btn
    panel <= body
    panel <= html.DIV(id="review-status", Class="dispatch-status")

    # Enter inside the PR input triggers the same dispatch path as the
    # button click. Same pattern as the resolve-issue-manual binding.
    def _on_review_keypress(ev):
        if getattr(ev, "key", "") == "Enter":
            _trigger_review()
    pr_input.bind("keypress", _on_review_keypress)


def _trigger_review(ev=None):
    status = document["review-status"]
    repo = document["review-repo"].value
    try:
        pr_num = document["review-pr-manual"].value.strip()
    except Exception:
        pr_num = ""
    if not pr_num:
        status.html = '<span class="dispatch-status-err">Type a PR number</span>'
        return
    if not pr_num.isdigit():
        status.html = '<span class="dispatch-status-err">PR number must be digits only</span>'
        return
    status.html = '<span class="spinner"></span> Dispatching...'
    gh_post(f"/repos/{repo}/issues/{pr_num}/comments",
            {"body": "/ai-review"},
            lambda req: _on_dispatch(req, "review-status"))
