"""Dispatch panels — AI Resolve (Plane issues) + AI Review (open PRs).

AI Resolve restored to a Splide carousel (2026-05-01) reading from the
static `data/issues.json` snapshot generated by the plane-snapshot workflow
every 15 min. The original carousel was removed in PR #12 because Plane
API blocks browser-origin CORS, but PR #14 introduced the same-origin
snapshot which sidesteps that entirely. Cards are colour-coded by `scope`
(front/back/both/other) computed in the snapshot generator — sprint-planning
visual cue so devs can scan and pick.

AI Review stays on the simple manual-input shape — most of the time you
already know the PR number from the link you just opened.
"""

from browser import ajax, document, html, window

from .api import gh_post
from .carousel import SplideCarousel
from .config import PLANE_PROJECT_IDENTIFIER, PLANE_WORKSPACE, REPO
from .start_work import open_modal as open_start_work_modal


# ── Shared ───────────────────────────────────────────────────────

_REPOS = [
    ("frontend-core-2.0", "Visalaw/frontend-core-2.0"),
    ("visalaw-gen-backend", "Visalaw/visalaw-gen-backend"),
    ("visalaw-gen-production-frontend", "Visalaw/visalaw-gen-production-frontend"),
]

_SNAPSHOT_URL = "data/issues.json"


def _on_dispatch(req, status_id):
    status = document[status_id]
    if req.status in (204, 201):
        status.html = '<span class="dispatch-status-ok">&#10003; Dispatched successfully</span>'
    else:
        import json as _json
        try:
            msg = _json.loads(req.text).get("message", f"HTTP {req.status}")
        except Exception:
            msg = f"HTTP {req.status}"
        status.html = f'<span class="dispatch-status-err">Error: {msg}</span>'


def _render_repo_select(select_id, include_all=False):
    sel = html.SELECT(id=select_id, Class="filter-select")
    if include_all:
        sel <= html.OPTION("All repos", value="all")
    for label, value in _REPOS:
        sel <= html.OPTION(label, value=value)
    return sel


def _open_external(ev, url, label):
    """Open URL in new tab with confirmation."""
    ev.stopPropagation()
    ev.preventDefault()
    if window.confirm(f"Open {label}?\n\nThis will open a new tab outside the dashboard."):
        window.open(url, "_blank")


